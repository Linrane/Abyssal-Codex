def get_enemy_art(enemy_id: str) -> list[str]:
    """Get ASCII art for an enemy by ID."""
    return DEFAULT_ART.get(enemy_id, GENERIC_ART)


def get_boss_title(boss_name: str, width: int = 40) -> str:
    """Render a decorative boss title."""
    return f"╔{'═' * width}╗\n║{boss_name:^{width}}║\n╚{'═' * width}╝"
